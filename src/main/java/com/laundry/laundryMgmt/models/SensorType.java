package com.laundry.laundryMgmt.models;

/**
 * @author  David PETREL
 * @version 1.0
 *
 * Represents the types of sensors used in the laundry management system.
 */
public enum SensorType {
    /**
     * Sensor type for temperature measurement.
     */
    TEMPERATURE,

    /**
     * Sensor type for humidity measurement.
     */
    HUMIDITY,

    /**
     * Sensor type for light functionality.
     */
    LIGHT,

    /**
     * Sensor type for level functionality.
     */
    LEVEL
}
